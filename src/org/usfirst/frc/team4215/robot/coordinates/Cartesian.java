package org.usfirst.frc.team4215.robot.coordinates;

public class Cartesian {

	private int x;	
	private int y;

	public Cartesian(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}

	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}
}
